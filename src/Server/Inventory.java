package Server;

public class Inventory {
	int cups;
	double coffee;
	double milk;
	double sugar;
	double balance;
	
	public Inventory(int cups, double coffee, double milk, double sugar, double balance){
		this.cups = cups;
		this.coffee = coffee;
		this.milk = milk;
		this.sugar = sugar;
		this.balance = balance;
	}
	
	
}
